// Demonstration for the use of trigonometric functions
// 1. cos(x). 2. sin(x). 3. tan(x).
#include <iostream.h>
#include <math.h>
void main()
{
	int x = 90;
	cout << "The cosine of angle 90 degree is : " << cos(x);
	cout << "\nThe sine of angle 90 degree is : " << sin(x);
	cout << "\nThe tangent of angle 90 degree is : " << tan(x);
}
